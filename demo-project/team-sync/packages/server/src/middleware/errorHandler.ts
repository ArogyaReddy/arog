import { Request, Response, NextFunction } from 'express';

export function errorHandler(
  err: Error,
  _req: Request,
  res: Response,
  _next: NextFunction
): void {
  console.error('‚ùå Error:', err);

  res.status(500).json({
    success: false,
    error: err.message || 'Internal server error',
    timestamp: new Date()
  });
}
